"""4) ახსენით კომენტარებით როგორ მუშაობს: lower, upper, capitalize მეთოდები

"""

# lower() მეთოდი:
# გადაჰყავს სტრიქონი პატარა ასოებზე
text = "Hello World"
print(text.lower())  # შედეგი იქნება: "hello world"

# upper() მეთოდი:
# გადაჰყავს სტრიქონი დიდი ასოებზე
text = "Hello World"
print(text.upper()) 

# capitalize() მეთოდი:
# მხოლოდ პირველი ასო გადაჰყავს დიდ ასოზე, ხოლო დანარჩენები პატარა ასოებზე
text = "hello world"
print(text.capitalize())